    @Override
    public void show() {
        super.show();
        try {
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/brjd", "root", "root");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT Subject,Marks,MaxMarks,Remark FROM results WHERE ExamRoll="+examroll+" AND ExamCode="+examcode+";");
            if (!rs.isBeforeFirst() ) {
                JOptionPane.showMessageDialog(null,"No result found! Please check your Roll Number");
            }else{
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                int rowCount = model.getRowCount();
                for (int i = rowCount - 1; i >= 0; i--) {
                    model.removeRow(i);
                }
                while(rs.next()){
                    String subject = rs.getString("Subject");
                    String marks = rs.getString("Marks");
                    String maxmarks = rs.getString("MaxMarks");
                    String remark = rs.getString("Remark");
                    model.addRow(new Object[]{subject,marks,maxmarks,remark});
                }
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Add_User.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Add_User.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

